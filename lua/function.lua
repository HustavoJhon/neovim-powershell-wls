  local width = 40;

  local function button(text, sc, execute, hl)
    if hl == nil then
      hl = "DevIconwoff"
    end

    local opts = {
      position = "center",
      width = width,
      shortcut = sc,
      cursor = 5,
      align_shortcut = "right",
      --hl_shortcut = "String",
      hl = hl,
      hl_shortcut = "Comment",
      shrink_margin = false,
    }

    if execute then
      opts.keymap = { "n", sc, execute, { noremap = false, silent = true, nowait = true } }
    end

    local function on_press()
      local key = vim.api.nvim_replace_termcodes(execute, true, false, true)
      vim.api.nvim_feedkeys(key, "normal", false)
    end

    return {
      type = "button",
      val = text,
      on_press = on_press,
      opts = opts
    }
  end

  local function workspace_button(name, number, path)
    return button(
      "  " .. name,
      tostring(number),
      "<cmd>cd " .. path .. "<CR><cmd>Alpha<CR><cmd>SessionsLoad<CR><cmd>NvimTreeOpen<CR>",
      {{"DevIconwoff", 3, width}, { "Title", 0, 3 }}
    )
  end

  local function padding(size)
    return {
      type = "padding",
      val = size
    }
  end

  local function text(t, hl)
    return {
      type = "text",
      val = t,
      opts = {
        position = "center",
        hl = hl,
      }
    }
  end

  local function cool_header(txt)
    return text("" .. txt .. "", {{"Title", 0, 3}, {"PmenuSel", 3, 3+txt:len()}, {"Title", 3+txt:len(), 6+txt:len()}})
  end

  local function section_header(txt)
    return {
      type = "group",
      val = {
        text(txt .. string.rep(' ', 40 - txt:len()), "Title"),
        text(string.rep('‾', txt:len()) .. string.rep(' ', 40 - txt:len()), "Title")
      }
    }
  end

  local function split(s, delimiter)
      local result = {};
      for match in (s..delimiter):gmatch("(.-)"..delimiter) do
          table.insert(result, match);
      end
      return result
  end

  local function reverse_table(t)
      local reversedTable = {}
      local itemCount = #t
      for k, v in ipairs(t) do
          reversedTable[itemCount + 1 - k] = v
      end
      return reversedTable
  end
  local function get_extension(fn)
      local match = fn:match("^.+(%..+)$")
      local ext = ""
      if match ~= nil then
          ext = match:sub(2)
      end
      return ext
  end

  local function icon(fn)
      local nwd = require("nvim-web-devicons")
      local ext = get_extension(fn)
      return nwd.get_icon(fn, ext, { default = true })
  end

  local function mru()
    local val = {
      section_header("Recents"),
      --text("‾‾‾‾‾‾‾                                  ", "Title"),
    }
    for _, oldfile in ipairs(vim.v.oldfiles) do
      if #val == 12 then
        break
      end
      if (vim.fn.filereadable(oldfile) == 1) then
        local s = split(oldfile, "/")
        local name = s[#s]
        for n,seg in ipairs(reverse_table(s)) do
          if n == 1 then
            goto continue
          end
          if name:len() + seg:len() + 1 > 26 then
            break
          end
          name = seg .. '/' .. name
          ::continue::
        end
        local ico, hl = icon(oldfile)
        local hl_tbl = {{ "DevIconwoff", 0, width }, { "Title", name:len() - s[#s]:len() + 5, name:len() + 5 }}
        if hl then
          table.insert(hl_tbl, { hl, 0, 5 })
        end

        val[#val+1] = button(ico .. "  " .. name, tostring(#val + #require('workspaces').get() - 1), "<cmd>e " .. oldfile .. "<CR>", hl_tbl)
      end
    end
    return val
  end

